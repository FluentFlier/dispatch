/**
 * One-shot generator for the FIXED 50-case canary subset (spec 4.3).
 * Deterministic: sorted inputs + stride sampling, so reruns on unchanged
 * case files produce an identical set. Regenerate only at the quarterly
 * case refresh (RUNBOOK); the output file is committed.
 *
 * Sources: all adversarial cases (capped 15) + stride-sampled core generated
 * cases. NEVER reads cases/holdout - that 20% stays unseen (Goodhart guard).
 * Profile fixtures are inlined so the serverless cron route needs no fs.
 *
 * NOTE on vars shape: the source yaml files (generate-cases.ts) only put
 * userPrompt/platform/contentType at the top-level `vars:` key - useVoice,
 * profileFixture, mentions, and sourceContext live ONLY inside the `caseJson`
 * string (see evals/shared.ts: "caseJson is the single source of truth").
 * So this generator parses caseJson to recover those fields rather than
 * reading them as top-level vars, which would silently find nothing.
 */
import { readFileSync, readdirSync, writeFileSync } from 'node:fs';
import { join } from 'node:path';
import { load } from 'js-yaml';
import type { CanaryCase, CanaryCaseVars } from '../src/lib/eval-canary/types';

interface RawCase { description?: string; vars?: Record<string, unknown> }

const TOTAL = 50;
const MAX_ADVERSARIAL = 15;
const casesDir = join(__dirname, 'cases');

function loadYamlCases(path: string): RawCase[] {
  const doc = load(readFileSync(path, 'utf8'));
  return Array.isArray(doc) ? (doc as RawCase[]) : [doc as RawCase];
}

const core = loadYamlCases(join(casesDir, 'core', 'generated.yaml'))
  .sort((a, b) => (a.description ?? '').localeCompare(b.description ?? ''));

const adversarial: RawCase[] = [];
for (const f of readdirSync(join(casesDir, 'adversarial')).filter((x) => x.endsWith('.yaml')).sort()) {
  adversarial.push(...loadYamlCases(join(casesDir, 'adversarial', f)));
}
adversarial.sort((a, b) => (a.description ?? '').localeCompare(b.description ?? ''));

const advPick = adversarial.slice(0, MAX_ADVERSARIAL);
const coreNeeded = TOTAL - advPick.length;
const stride = Math.max(1, Math.floor(core.length / coreNeeded));
const corePick: RawCase[] = [];
for (let i = 0; i < core.length && corePick.length < coreNeeded; i += stride) corePick.push(core[i]);
if (advPick.length + corePick.length < TOTAL) {
  throw new Error(`Only ${advPick.length + corePick.length} cases available; need ${TOTAL}. Grow the core set first.`);
}
// Stride sampling can overshoot coreNeeded by a few (int division rounding);
// trim back to the exact fixed size.
const finalCore = corePick.slice(0, coreNeeded);

function extractVars(raw: RawCase): CanaryCaseVars {
  const topVars = raw.vars ?? {};
  let fromCaseJson: Record<string, unknown> = {};
  if (typeof topVars.caseJson === 'string') {
    try {
      fromCaseJson = JSON.parse(topVars.caseJson);
    } catch {
      /* fall back to top-level vars only */
    }
  }
  return {
    userPrompt: (fromCaseJson.userPrompt as string) ?? (topVars.userPrompt as string) ?? '',
    platform: (fromCaseJson.platform as string) ?? (topVars.platform as string | undefined),
    contentType: (fromCaseJson.contentType as string) ?? (topVars.contentType as string | undefined),
    useVoice: fromCaseJson.useVoice as boolean | undefined,
    sourceContext: (fromCaseJson.sourceContext as string) ?? (topVars.sourceContext as string | undefined),
    mentions: (fromCaseJson.mentions as string[]) ?? (topVars.mentions as string[] | undefined),
    profileFixture: fromCaseJson.profileFixture as string | undefined,
  };
}

const cases: CanaryCase[] = advPick.concat(finalCore).map((c, i) => ({
  id: `canary-${String(i).padStart(2, '0')}`,
  description: c.description ?? `unnamed/case-${i}`,
  vars: extractVars(c),
}));

// Inline profile fixtures (serverless cron cannot fs-read evals/fixtures).
for (const c of cases) {
  if (c.vars.profileFixture) {
    const fixture = JSON.parse(
      readFileSync(join(__dirname, 'fixtures', 'profiles', c.vars.profileFixture), 'utf8'),
    );
    c.vars.inlineFixture = fixture;
  }
}

const outPath = join(__dirname, '..', 'src', 'lib', 'eval-canary', 'cases.ts');
const banner =
  '// GENERATED by evals/generate-canary.ts. Do not hand-edit.\n' +
  '// Regenerate at the quarterly case refresh (docs/RUNBOOK-generation-ops.md).\n';
writeFileSync(
  outPath,
  `${banner}import type { CanaryCase } from './types';\n\nexport const CANARY_CASES: CanaryCase[] = ${JSON.stringify(cases, null, 2)};\n`,
);
console.log(`wrote ${cases.length} cases (${advPick.length} adversarial) to ${outPath}`);
