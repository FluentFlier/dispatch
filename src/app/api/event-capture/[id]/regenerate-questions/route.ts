import { NextRequest, NextResponse } from 'next/server';
import { getAuthenticatedUser, getServerClient } from '@/lib/insforge/server';
import { getActiveWorkspaceId } from '@/lib/workspace';
import { enrichCapture } from '@/lib/event-capture/enrich';

interface RouteParams {
  params: { id: string };
}

/**
 * POST /api/event-capture/[id]/regenerate-questions
 * Reloads the question set for ONE event without re-importing the whole calendar.
 * Clears stored answers first (the caller confirms this in the UI), then re-runs
 * Stage-2 enrichment (fresh research + new LLM questions) and returns to
 * 'questions_ready'. Because questions are generated by the LLM each call, this
 * produces a genuinely different set — not the previously cached one.
 */
export async function POST(
  _request: NextRequest,
  { params }: RouteParams,
): Promise<NextResponse> {
  const user = await getAuthenticatedUser();
  if (!user) return NextResponse.json({ error: 'Unauthorized' }, { status: 401 });

  const workspaceId = await getActiveWorkspaceId(user.id);
  if (!workspaceId) return NextResponse.json({ error: 'No active workspace' }, { status: 400 });

  const client = getServerClient();

  // Ownership check before doing any work.
  const { data: capture } = await client.database
    .from('event_captures')
    .select('id, workspace_id')
    .eq('id', params.id)
    .eq('workspace_id', workspaceId)
    .single();

  if (!capture) return NextResponse.json({ error: 'Capture not found' }, { status: 404 });

  try {
    // Confirm-then-clear: wipe answers so the fresh questions start clean.
    await client.database
      .from('event_captures')
      .update({ answers: {}, updated_at: new Date().toISOString() })
      .eq('id', params.id)
      .eq('workspace_id', workspaceId);

    const outcome = await enrichCapture(client, params.id, new Date(), { ignoreRecency: true });
    if (outcome !== 'questions_ready') {
      return NextResponse.json({ error: `Could not reload questions (${outcome})` }, { status: 502 });
    }

    return NextResponse.json({ ok: true, status: 'questions_ready' });
  } catch (err) {
    console.error('[event-capture/regenerate-questions] error', err);
    return NextResponse.json({ error: 'Failed to reload questions' }, { status: 500 });
  }
}
